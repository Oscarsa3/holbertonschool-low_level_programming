#include "3-calc.h"
/**
 * op_add - funcion suma
 * @a: primer numero
 * @b: segundo numero
 * Return: suma
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - funcion resta
 * @a: primer numero
 * @b: segundo numero
 * Return: resta
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - funcion multiplicacion
 * @a: primer numero
 * @b: segundo numero
 * Return: multiplicacion
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - funcion division
 * @a: primer numero
 * @b: segundo numero
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - funcion modulo
 * @a: primer numero
 * @b: segundo numero
 * Return: modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
